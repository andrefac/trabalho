package com.example.brunab.entidade;

public class ent_cliente {

    private String cNome;
    private String cCpf;
    private String cRg;
    private String cCnh;
    private String cDependente;
    private String cEndereco;

    public String getcNome() {
        return cNome;
    }

    public void setcNome(String cNome) {
        this.cNome = cNome;
    }

    public String getcCpf() {
        return cCpf;
    }

    public void setcCpf(String cCpf) {
        this.cCpf = cCpf;
    }

    public String getcRg() {
        return cRg;
    }

    public void setcRg(String cRg) {
        this.cRg = cRg;
    }

    public String getcCnh() {
        return cCnh;
    }

    public void setcCnh(String cCnh) {
        this.cCnh = cCnh;
    }

    public String getcDependente() {
        return cDependente;
    }

    public void setcDependente(String cDependente) {
        this.cDependente = cDependente;
    }

    public String getcEndereco() {
        return cEndereco;
    }

    public void setcEndereco(String cEndereco) {
        this.cEndereco = cEndereco;
    }
}
